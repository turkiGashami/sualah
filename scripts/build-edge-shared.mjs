// Bundle @sualah/game-core into a single Deno-friendly ESM module that the
// Edge Functions import. This keeps the game rules in ONE place (game-core) and
// reuses them verbatim server-side — no re-implementation. esbuild resolves the
// package's ".js" import specifiers to their ".ts" sources automatically.
import { build } from "esbuild";
import { mkdirSync } from "node:fs";

mkdirSync("supabase/functions/_shared", { recursive: true });

await build({
  entryPoints: ["packages/game-core/src/index.ts"],
  outfile: "supabase/functions/_shared/game-core.js",
  bundle: true,
  format: "esm",
  platform: "neutral",
  target: "es2022",
  banner: {
    js: "// GENERATED by scripts/build-edge-shared.mjs from packages/game-core — do not edit.",
  },
});

console.log("✓ built supabase/functions/_shared/game-core.js");
